const frameMap = [1, 2, 3, 4, 5, 6, 7, 8, 7, 6, 5, 4, 3, 2, 1];

export default {

	data()
	{
		return {
			timer: null,
			counter: 0,
		}
	},

	methods:
	{
		onEnter()
		{
			// console.log('onEnter')
			this.timer && clearInterval(this.timer);
			this.timer = setInterval(() =>
			{
				this.counter = (this.counter+1) % frameMap.length;
			}, 80);
		},

		onLeave()
		{
			// console.log('onLeave')
			this.timer && clearInterval(this.timer);
			this.timer = null;
			this.counter = 0;
		},

		getStyleFromFrame(frameNum)
		{
			return {
				display: frameMap[this.counter]==frameNum ? 'block' : 'none'
			}
		}
	}
}
